// 현재 언어 감지 및 가져오기
function detectLanguage() {
    // 1. 저장된 언어 우선
    const saved = localStorage.getItem('emojiLang');
    if (saved && translations[saved]) return saved;
    // 2. 브라우저 언어 감지
    const browserLang = navigator.language || navigator.userLanguage || 'en';
    // zh-TW, zh-Hant 등 처리
    if (browserLang.startsWith('zh') && (browserLang.includes('TW') || browserLang.includes('Hant') || browserLang.includes('HK'))) return 'zh-TW';
    if (browserLang.startsWith('zh')) return 'zh';
    const short = browserLang.split('-')[0];
    if (translations[short]) return short;
    return 'en';
}

function t(key) {
    const lang = window.currentLang || 'en';
    return (translations[lang] && translations[lang][key]) || translations['en'][key] || key;
}

function setLanguage(lang) {
    window.currentLang = lang;
    localStorage.setItem('emojiLang', lang);
    // RTL 지원 (아랍어)
    document.documentElement.dir = lang === 'ar' ? 'rtl' : 'ltr';
    document.documentElement.lang = lang;
}
